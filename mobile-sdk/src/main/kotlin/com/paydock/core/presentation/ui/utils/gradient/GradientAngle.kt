package com.paydock.core.presentation.ui.utils.gradient

/**
 * Enum class representing different gradient angles in a clockwise direction.
 */
enum class GradientAngle {
    CW0, // 0 degrees
    CW45, // 45 degrees
    CW90, // 90 degrees
    CW135, // 135 degrees
    CW180, // 180 degrees
    CW225, // 225 degrees
    CW270, // 270 degrees
    CW315 // 315 degrees
}